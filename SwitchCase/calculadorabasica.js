let operacao = '+'
let num1 = 10
let num2 = 20

switch (operacao) {
    case '+':
        console.log (num1 + num2)
        break
    case '-':
        console.log (num1 - num2)
        break
    case '*':
        console.log (num1 * num2)
        break
    case '/':
        console.log (num1 / num2)
        break
}